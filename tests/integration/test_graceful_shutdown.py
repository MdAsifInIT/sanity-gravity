# Dynamic compose is now generated by the sanity_gravity package; import the
# helper from the new location.
import sys
sys.path.insert(0, os.path.abspath(
    os.path.join(os.path.dirname(__file__), "..", "..")
))


def _load_sanity_cli():
    """Return the compose-generator module so callers can keep reading
    ``cli.generate_compose_for_tag(...)``."""
    from sanity_gravity.compose import generators as cg
    return cg


ENTRYPOINT_FILE = os.path.join(
    os.path.dirname(__file__),
    "..", "..", "sandbox", "rootfs", "usr", "local", "bin", "entrypoint.sh",
)
AG_SHUTDOWN_FILE = os.path.join(
    os.path.dirname(__file__),
    "..", "..", "plugins", "agents", "ag", "rootfs", "etc", "shutdown.d", "10-ag-shutdown.sh",
)
DOCKERFILE_BASE = os.path.join(
    os.path.dirname(__file__),
    "..", "..", "sandbox", "Dockerfile.base",
)

CONTAINER_STARTUP_TIMEOUT = 30  # seconds to wait for kasm + X session


def _load_compose_for_tag(tag):
    cli = _load_sanity_cli()
    compose_path, _ = cli.generate_compose_for_tag(tag)
    with open(compose_path) as f:
        return yaml.safe_load(f)


def _read_file(path):
    with open(path) as f:
        return f.read()


def _run(cmd, check=False):
    return subprocess.run(cmd, shell=True, capture_output=True, text=True, check=check)


def _docker_exec(container, cmd, user=None):
    if user:
        full = f"docker exec {container} su - {user} -c {shlex.quote(cmd)}"
    else:
        full = f"docker exec {container} bash -c {shlex.quote(cmd)}"
    return _run(full)


