class TestGracefulShutdownConfig:
    """Verify static configuration for graceful shutdown."""

    def test_stop_grace_period_kasm(self):
        """Dynamic compose for kasm connector includes stop_grace_period."""
        compose = _load_compose_for_tag("ag-xfce-kasm")
        assert compose["services"]["ag-xfce-kasm"]["stop_grace_period"] == "30s"

    def test_stop_grace_period_vnc(self):
        """Dynamic compose for vnc connector includes stop_grace_period."""
        compose = _load_compose_for_tag("ag-xfce-vnc")
        assert compose["services"]["ag-xfce-vnc"]["stop_grace_period"] == "30s"

    def test_entrypoint_has_sigterm_trap(self):
        content = _read_file(ENTRYPOINT_FILE)
        assert "trap graceful_shutdown SIGTERM" in content

    def test_ag_shutdown_hook_has_xdotool_ctrl_q(self):
        content = _read_file(AG_SHUTDOWN_FILE)
        assert "xdotool key --window" in content
        assert "ctrl+q" in content

    def test_entrypoint_does_not_exec_supervisord(self):
        """PID 1 must be bash (not supervisord) so trap works."""
        content = _read_file(ENTRYPOINT_FILE)
        lines = content.strip().splitlines()
        tail = "\n".join(lines[-5:])
        assert 'exec "$@"' not in tail

    def test_entrypoint_runs_supervisord_in_background(self):
        content = _read_file(ENTRYPOINT_FILE)
        assert '"$@" &' in content
        assert "SUPERVISOR_PID=$!" in content

    def test_entrypoint_waits_for_supervisor(self):
        content = _read_file(ENTRYPOINT_FILE)
        assert 'wait "$SUPERVISOR_PID"' in content

    def test_entrypoint_forwards_sigterm_to_supervisor(self):
        content = _read_file(ENTRYPOINT_FILE)
        assert 'kill -TERM "$SUPERVISOR_PID"' in content

    def test_dockerfile_installs_xdotool(self):
        content = _read_file(DOCKERFILE_BASE)
        # xdotool is in the desktop layer, check base has core deps
        # or check the xfce desktop layer
        xfce_layer = os.path.join(
            os.path.dirname(__file__),
            "..", "..", "plugins", "desktops", "xfce", "Dockerfile",
        )
        layer_content = _read_file(xfce_layer)
        assert "xdotool" in layer_content

    def test_ag_shutdown_hook_filters_helper_windows(self):
        """Must filter by window name to skip antigravity-bin helper windows."""
        content = _read_file(AG_SHUTDOWN_FILE)
        assert '" - Antigravity"' in content

