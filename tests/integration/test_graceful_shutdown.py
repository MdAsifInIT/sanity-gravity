@pytest.fixture(scope="class")
def graceful_container(request):
    """Spin up a kasm container for graceful shutdown testing.

    Uses scope="class" so the container is shared across tests in a class
    and rebuilt (via docker start) when a test stops it.
    """
    name = "sanity-test-graceful-shutdown"
    uid = str(os.getuid())
    gid = str(os.getgid())
    user = os.getenv("USER", "testuser")

    # Clean up any leftover from previous runs
    _run(f"docker rm -f {name}")

    # Start container (--rm=False so we can restart it)
    env_flags = (
        f"-e HOST_UID={uid} -e HOST_GID={gid} "
        f"-e HOST_USER={user} -e HOST_PASSWORD=testpassword"
    )
    _run(
        f"docker run -d --name {name} --shm-size=512m "
        f"{env_flags} {DEFAULT_KASM_IMAGE}",
        check=True,
    )

    # Wait for kasm + X session to be ready
    assert wait_for_log(name, "success: kasmvnc", timeout=CONTAINER_STARTUP_TIMEOUT), \
        "Container did not start kasmvnc in time"

    yield name, user

    # Cleanup
    _run(f"docker rm -f {name}")


class TestGracefulShutdownIntegration:
    """Integration tests that spin up their own kasm container."""

    def test_xdotool_available(self, graceful_container):
        name, user = graceful_container
        r = _docker_exec(name, "which xdotool", user=user)
        assert r.returncode == 0, "xdotool not found in container"

    def test_pid1_is_entrypoint_not_supervisord(self, graceful_container):
        """PID 1 must be the entrypoint shell (not supervisord) for trap to work."""
        name, _ = graceful_container
        r = _docker_exec(name, "cat /proc/1/comm")
        comm = r.stdout.strip()
        assert comm not in ("supervisord", "python3"), \
            f"PID 1 is {comm}, expected entrypoint shell"

    def test_supervisor_running(self, graceful_container):
        name, _ = graceful_container
        r = _docker_exec(name, "pgrep -c supervisord")
        assert int(r.stdout.strip()) >= 1

    def test_docker_stop_triggers_graceful_handler(self, graceful_container):
        """docker stop should trigger the SIGTERM trap and log the shutdown message."""
        name, _ = graceful_container

        # Stop the container (SIGTERM → trap → graceful_shutdown())
        r = _run(f"docker stop -t 30 {name}")
        assert r.returncode == 0, f"docker stop failed: {r.stderr}"

        # Check container logs for the graceful shutdown message
        logs = _run(f"docker logs {name}").stdout + _run(f"docker logs {name}").stderr
        assert "[shutdown] Graceful shutdown initiated" in logs, \
            f"Graceful shutdown handler was not triggered. Logs:\n{logs[-500:]}"

        # Restart for subsequent tests
        _run(f"docker start {name}", check=True)
        assert wait_for_log(name, "Starting Antigravity Sandbox", timeout=CONTAINER_STARTUP_TIMEOUT)
        # Wait for supervisord to be fully ready
        time.sleep(3)

    def test_docker_restart_triggers_graceful_handler(self, graceful_container):
        """docker restart should also trigger the SIGTERM trap."""
        name, _ = graceful_container

        # Clear previous logs by noting current log length
        logs_before = _run(f"docker logs {name}").stdout
        marker_count_before = logs_before.count("[shutdown] Graceful shutdown initiated")

        # Restart the container
        r = _run(f"docker restart -t 30 {name}")
        assert r.returncode == 0, f"docker restart failed: {r.stderr}"

        # Wait for container to be back up
        assert wait_for_log(name, "Starting Antigravity Sandbox", timeout=CONTAINER_STARTUP_TIMEOUT)
        time.sleep(3)

        # Check that a NEW graceful shutdown message appeared
        logs_after = _run(f"docker logs {name}").stdout
        marker_count_after = logs_after.count("[shutdown] Graceful shutdown initiated")
        assert marker_count_after > marker_count_before, \
            "docker restart did not trigger a new graceful shutdown"

    def test_supervisor_recovers_after_restart(self, graceful_container):
        """After docker restart, supervisord should be running again."""
        name, _ = graceful_container
        r = _docker_exec(name, "pgrep -c supervisord")
        assert int(r.stdout.strip()) >= 1
