"""Tests for Antigravity graceful shutdown mechanism.

Static tests verify configuration correctness (no Docker required).
Integration tests spin up a kasm container and verify actual docker stop/restart
triggers the SIGTERM trap and graceful shutdown handler in entrypoint.sh.
"""
import pytest
import subprocess
import os
import shlex
import time

import yaml

from tests.utils import wait_for_log
